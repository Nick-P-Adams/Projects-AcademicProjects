.ORIG x3000
LDI R1 XPTR
LDI R2 YPTR

;SUM of X and Y
ADD R3 R1 R2
STI R3 SUM
AND R3 R3 #0

;X AND Y
AND R3 R1 R2
STI R3 YANDX
AND R3 R3 #0

;X OR Y
NOT R1 R1
NOT R2 R2
AND R3 R1 R2
NOT R3 R3
STI R3 XORY
LDI R1 XPTR
LDI R2 YPTR
AND R3 R3 #0

;NOT(X)
NOT R3 R1
STI R3 NOTX
AND R3 R3 #0

;NOT(Y)
NOT R3 R2
STI R3 NOTY
AND R3 R3 #0

;SUM of X + 3
ADD R3 R1 #3
STI R3 XPL3
AND R3 R3 #0

;SUM Y - 3
ADD R3 R2 #-3
STI R3 YMI3
AND R3 R3 #0

;EVEN or ODD
AND R3 R1 #1
STI R3 EO
AND R3 R3 #0

HALT
XPTR .FILL x3100 
YPTR .FILL x3101

SUM .FILL x3102
YANDX .FILL x3103
XORY .FILL x3104
NOTX .FILL x3105
NOTY .FILL x3106
XPL3 .FILL x3107
YMI3 .FILL x3108
EO .FILL x3109
.END